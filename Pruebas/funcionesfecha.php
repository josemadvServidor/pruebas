	}
	if ($dia == 2)
	{
		return 'Martes';
	}
	if ($dia == 3)
	{
		return 'Miercoles';
	}
	if ($dia == 4)
	{
		return 'Jueves';
	}
	if ($dia == 5)
	{
		return 'Viernes';
	}
	if ($dia == 6)
	{
		return 'Sabado';
	}
	if ($dia == 7)
	{
		return 'Domingo';
	}
}

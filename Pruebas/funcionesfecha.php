<?php
function NombreMes($mes)
{
	if ($mes == 1)
	{
		return 'Enero';
	}
	if ($mes == 2)
	{
		return 'Febrero';
	}
	if ($mes == 3)
	{
		return 'Marzo';
	}
	if ($mes == 4)
	{
		return 'Abril';
	}
	if ($mes == 5)
	{
		return 'Mayo';
	}
	if ($mes == 6)
	{
		return 'Junio';
	}
	if ($mes == 7)
	{
		return 'Julio';
	}
	if ($mes == 8)
	{
		return 'Agosto';
	}
	if ($mes == 9)
	{
		return 'Septiembre';
	}
	if ($mes == 10)
	{
		return 'Octubre';
	}
	if ($mes == 11)
	{
		return 'Noviembre';
	}
	if ($mes == 12)
	{
		return 'Diciembre';
	}
}

function NombreDia($dia)
{
	if ($dia == 1)
